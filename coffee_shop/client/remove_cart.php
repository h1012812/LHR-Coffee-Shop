<?php

// ============================
// بدء الجلسة لاستخدام بيانات السلة
// ============================
session_start();

// ============================
// التحقق من وجود رقم المنتج في الرابط
// ============================
if (isset($_GET['id'])) {

    // تحويل رقم المنتج إلى قيمة آمنة
    $id = $_GET['id'];

    // ============================
    // البحث عن المنتج داخل مصفوفة السلة
    // array_search يعيد رقم الفهرس إذا وجد العنصر
    // ============================
    if (($key = array_search($id, $_SESSION['cart'])) !== false) {

        // ============================
        // حذف المنتج من السلة باستخدام unset
        // ============================
        unset($_SESSION['cart'][$key]);
    }

    // ============================
    // إعادة التوجيه إلى صفحة السلة بعد الحذف
    // ============================
    header("Location: cart.php");
    exit();
}
